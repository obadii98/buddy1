package com.bracketsps.buddy_final.ui.allFragment;

public class Result {
    int cal;
    int pro;

    public Result(int cal, int pro){
        this.cal=cal;
        this.pro=pro;
    }

    public int getCal() {
        return cal;
    }

    public int getPro() {
        return pro;
    }

    public void setCal(int cal) {
        this.cal = cal;
    }

    public void setPro(int pro) {
        this.pro = pro;
    }
}
